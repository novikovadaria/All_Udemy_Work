# ..................................................Numbers.....................
# Integer
age = 17
print(age)
print(type(age))
# Foats
weight = 48.8
print(weight)
print(type(weight))
# ..................................................Booleans......................
telegram = True
watsapp = False
print(telegram, watsapp)
print(type(telegram))
print(type(watsapp))
# ..................................................Strings.......................
fish_name = "Smarty"
print(fish_name)
print(type(fish_name))
# ....................................................List..........................
# ordered
number = [1, 2, 3, 4.5, 6.7]
print(number)
print(type(number))
mixed = [1, False, "Zhenya", 3.55, [1, 2, 3]]
print(mixed)
print(type(mixed))
# ...................................................Dictionary.................
# unordered
user_info = {"users_favourite_type_of_music": "rock"}
print(type(user_info))
# .....................................................Tuple...............................
mixed = (1, False, "Zhenya", 3.55, [1, 2, 3])
print(type(mixed))
# .......................................................Set.......................
# unordered
mixed_set = {1, 2, 3, 4, True}
print(type(mixed_set))
